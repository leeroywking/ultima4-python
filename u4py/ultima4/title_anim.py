class ViewAnim:
    """Runs the title view's sprite script (C: TITLE_0.C C_041A). `tick()` advances one frame;
    `sprites()` yields the active (x, y, tile) sprites to draw over the base map."""
    def __init__(self):
        self.i = -1
        self.x = [0] * 16
        self.y = [0] * 16
        self.tile = [0] * 16
        self.delay = 0
        self.advance()

    def tick(self) -> None:
        if self.delay > 0:                              # C: D_00C6 frame-hold
            self.delay -= 1
            return
        self.advance()

    def advance(self) -> None:
        while True:
            self.i += 1
            if self.i >= len(VIEW_SCRIPT):
                self.i = 0
            b = VIEW_SCRIPT[self.i]
            hi = b & 0xf0
            if hi <= 0x60:                              # placement: slot=low nibble, y=high nibble
                s = b & 0xf
                self.y[s] = b >> 4
                self.i += 1
                n = VIEW_SCRIPT[self.i]                 # next byte: x (low 5) + frame (high 3)
                self.x[s] = n & 0x1f
                self.tile[s] = (VIEW_BASE[s] + (n >> 5)) if s < len(VIEW_BASE) else 0
            elif hi == 0x70:                            # remove slot
                self.tile[b & 0xf] = 0
            elif hi == 0x80:                            # end frame (+ hold delay)
                self.delay = b & 0xf
                return
            else:                                       # 0x90+/0xFF: restart the script
                self.i = -1

    def sprites(self):
        return [(self.x[s], self.y[s], self.tile[s]) for s in range(16) if self.tile[s]]
