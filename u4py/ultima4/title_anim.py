def crop_frames(animate_surface):
    """Slice ANIMATE into the two monsters' 18 frames each: (frames1, frames2)."""
    f1 = [animate_surface.subsurface((MON1_SRC_X[f] * 8, SRC_Y[f], FRAME_W, FRAME_H))
          for f in range(18)]
    f2 = [animate_surface.subsurface((MON2_SRC_X[f] * 8, SRC_Y[f], FRAME_W, FRAME_H))
          for f in range(18)]
    return f1, f2


def frame_indices(step: int):
    """The (monster1, monster2) frame indices at animation `step`. C: D_0036/D_0038 counters."""
    return SEQ1[step % len(SEQ1)], SEQ2[step % len(SEQ2)]


